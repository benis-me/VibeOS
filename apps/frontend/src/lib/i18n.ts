const DICT: Record<Locale, Dict> = { zh, en };

/** Browser-derived default when the user hasn't chosen a language yet. */
export function browserLocale(): Locale {
  if (typeof navigator !== "undefined" && navigator.language.toLowerCase().startsWith("en")) {
    return "en";
  }
  return "zh";
}

export function translate(locale: Locale, key: string): string {
  return DICT[locale][key] ?? DICT.zh[key] ?? key;
}

/** Current effective locale: the chosen one, else the browser default. */
export function useLocale(): Locale {
  const chosen = useSettingsStore((s) => s.settings?.locale);
  return chosen ?? browserLocale();
}

/** Translator bound to the current locale. */
export function useT(): (key: string) => string {
  const locale = useLocale();
  return (key: string) => translate(locale, key);
}
